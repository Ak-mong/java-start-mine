package loop.ex;

public class WhileEx {
    public static void main(String[] args) {
        int count = 0;
        while (count < 10) {
            count++;
            System.out.println(count);
        }
        System.out.println();
        for(count=1;count<=10;count++){
            System.out.println(count);
        }
    }
}
